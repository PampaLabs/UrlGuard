namespace UrlGuard;

/// <summary>
/// Static class that defines parameter names for signed URLs.
/// </summary>
public static class SignedUrlQueryParams
{
    /// <summary>
    /// Name of the parameter used for URL signature.
    /// </summary>
    public const string Sig = "sig";

    /// <summary>
    /// Name of the parameter used for URL expiration.
    /// </summary>
    public const string Exp = "exp";
}
